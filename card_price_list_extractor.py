    for image_path in tqdm(processed_images, desc="Ekstraksi data AI"):
        img_pil = Image.open(image_path)  # Gambar PIL untuk Gemini

        for attempt in range(max_retries):
            try:
                response = client.models.generate_content(
                    model=MODEL,
                    contents=[PROMPT, img_pil]
                )

                text = response.text.strip()
                text = text.replace("```json", "").replace("```", "")
                data = json.loads(text)
                all_packages.extend(data)
                break  # Keluar dari loop retry jika berhasil

            except Exception as e:
                error_msg = str(e)
                if "503" in error_msg or "UNAVAILABLE" in error_msg or "429" in error_msg:
                    print(f"⚠️ API Limit / Server Sibuk (Percobaan {attempt+1}/{max_retries} untuk {image_path.name}). Mencoba API Key baru...")
                    client = get_new_gemini_client()
                    time.sleep(2)  # Beri jeda agar client baru stabil
                else:
                    print(f"❌ Gagal Parse JSON atau Error Lainnya untuk {image_path.name}: {error_msg}")
                    if 'response' in locals() and hasattr(response, 'text'):
                        print(response.text)
                    break  # Hentikan percobaan ulang untuk gambar ini jika error bukan karena kuota/server sibuk

    print(f"✅ Ekstraksi AI selesai. Total {len(all_packages)} paket ditemukan.")

    if not all_packages:
        print("⚠️ Tidak ada paket yang berhasil diekstrak. Tidak dapat membuat laporan.")
        return

    # 5. Generate Excel Report
    print("\n📊 Langkah 5: Membuat laporan Excel...")
    df_clean = pd.DataFrame(all_packages)

    # Normalisasi Provider
    provider_mapping = {
        'INDOSAT': 'IM3', 'THREE': '3ID', 'TELKOMSEL': 'TSEL',
        'SMARTFREN': 'SF', 'XL': 'XL', 'AXIS': 'AXIS'
    }
    df_clean['provider'] = df_clean['provider'].astype(str).str.upper().map(provider_mapping).fillna(df_clean['provider'].astype(str).str.upper())

    # Konversi tipe data dan kalkulasi Yield
    df_clean['price'] = pd.to_numeric(df_clean['price'], errors='coerce')
    df_clean['gb'] = pd.to_numeric(df_clean['gb'], errors='coerce')
    df_clean['days'] = pd.to_numeric(df_clean['days'], errors='coerce')
    df_clean = df_clean.dropna(subset=['price', 'gb', 'days'])  # Buang data tidak lengkap

    df_clean['yield'] = (df_clean['price'] / df_clean['gb']).apply(lambda x: math.ceil(x) if x != np.inf else 0)

    # Fungsi Kategorisasi
    def categorize(d, p):
        if d <= 2: return "SACHET 1D-2D"
        elif d == 3: return "SACHET 3D"
        elif d <= 5: return "SACHET 5D"
        elif d <= 7: return "SACHET 7D"
        elif d <= 15: return "SACHET 10D-15D"
        elif d >= 28 and p <= 50000: return "MONTHLY 30-50K"
        elif d >= 28 and p > 50000: return "MONTHLY > 50K"
        else: return "OTHER"

    df_clean['category'] = df_clean.apply(lambda row: categorize(row['days'], row['price']), axis=1)

    # Sortir data dari yang termurah per kategori
    df_clean = df_clean.sort_values(by=['category', 'provider', 'price'])

    # --- 2. EXCEL GENERATION (OPENPYXL) ---
    wb = Workbook()
    ws = wb.active
    ws.title = "Rekap Harga Automasi"

    HEADER_COLORS = {"IM3": "FFC000", "3ID": "FF00FF", "TSEL": "FF0000", "XL": "0070C0", "AXIS": "7030A0", "SF": "FF00FF"}
    providers_order = ["IM3", "3ID", "TSEL", "XL", "AXIS", "SF"]
    categories_order = ["SACHET 1D-2D", "SACHET 3D", "SACHET 5D", "SACHET 7D", "SACHET 10D-15D", "MONTHLY 30-50K", "MONTHLY > 50K"]

    font_bold_white = Font(bold=True, color="FFFFFF")
    align_center = Alignment(horizontal="center", vertical="center")
    border_thin = Border(left=Side(style='thin'), right=Side(style='thin'), top=Side(style='thin'), bottom=Side(style='thin'))
    border_red_dashed = Border(left=Side(style='mediumDashed', color='FF0000'), right=Side(style='mediumDashed', color='FF0000'),
                               top=Side(style='mediumDashed', color='FF0000'), bottom=Side(style='mediumDashed', color='FF0000'))

    # Generate Header
    col_start = 2
    for prov in providers_order:
        ws.merge_cells(start_row=1, start_column=col_start, end_row=1, end_column=col_start + 3)
        c = ws.cell(row=1, column=col_start, value=prov)
        c.fill, c.font, c.alignment, c.border = PatternFill(start_color=HEADER_COLORS.get(prov, "808080"), fill_type="solid"), font_bold_white, align_center, border_thin

        for i, sh in enumerate(["Price", "GB", "Days", "Yield"]):
            c_sub = ws.cell(row=2, column=col_start + i, value=sh)
            c_sub.fill = PatternFill(start_color="FFD966", fill_type="solid") if sh == "Yield" else PatternFill(start_color="D9D9D9", fill_type="solid")
            c_sub.font, c_sub.alignment, c_sub.border = Font(bold=True), align_center, border_thin
        col_start += 4

    # Generate Body Data Horizontal
    current_row = 3
    yield_cells = []  # Untuk menyimpan koordinat sel yang akan diberi warna heatmap

    for cat in categories_order:
        cat_df = df_clean[df_clean['category'] == cat]
        if cat_df.empty: continue

        prov_data = {prov: cat_df[cat_df['provider'] == prov].to_dict('records') for prov in providers_order}
        max_rows = max([len(v) for v in prov_data.values()] + [1])

        ws.merge_cells(start_row=current_row, start_column=1, end_row=current_row + max_rows - 1, end_column=1)
        c_cat = ws.cell(row=current_row, column=1, value=cat)
        c_cat.alignment, c_cat.border = Alignment(vertical="center", horizontal="center", wrap_text=True), border_thin

        for i in range(max_rows):
            col_idx = 2

            # Cari yield terkecil di baris ini untuk "Best Offer Red Dashed Box"
            row_yields = []
            for prov in providers_order:
                if i < len(prov_data[prov]): row_yields.append(prov_data[prov][i]['yield'])
            min_yield_in_row = min(row_yields) if row_yields else None

            for prov in providers_order:
                for offset in range(4):  # Set border abu-abu default untuk semua sel (meskipun kosong)
                    ws.cell(row=current_row + i, column=col_idx + offset).border = border_thin

                if i < len(prov_data[prov]):
                    pkg = prov_data[prov][i]

                    # Format Harga & Yield (Rp / Koma Ribuan)
                    ws.cell(row=current_row + i, column=col_idx, value=pkg['price']).number_format = '#,##0'
                    ws.cell(row=current_row + i, column=col_idx + 1, value=pkg['gb'])
                    ws.cell(row=current_row + i, column=col_idx + 2, value=pkg['days'])

                    c_yield = ws.cell(row=current_row + i, column=col_idx + 3, value=pkg['yield'])
                    c_yield.number_format = '#,##0'
                    yield_cells.append(c_yield.coordinate)  # Simpan posisi sel untuk heatmap warna

                    # Highlight best deal (Red dashed border)
                    if pkg['yield'] == min_yield_in_row and pkg['yield'] > 0:
                        for offset in range(4): ws.cell(row=current_row + i, column=col_idx + offset).border = border_red_dashed

                col_idx += 4
        current_row += max_rows

    # --- 3. APPLY CONDITIONAL FORMATTING (HEATMAP YIELD) ---
    # Format warna Hijau (murah) - Kuning (standar) - Merah (mahal)
    color_scale = ColorScaleRule(start_type='min', start_color='63BE7B', mid_type='percentile', mid_value=50, mid_color='FFEB84', end_type='max', end_color='F8696B')
    if yield_cells:
        # Buat range sel contoh: "E3:E15, I3:I15, dst"
        ws.conditional_formatting.add(f"E3:Z{current_row}", color_scale)

    # Auto-adjust column width
    for col_idx in range(1, ws.max_column + 1):
        column_letter = get_column_letter(col_idx)
        max_length = 0
        for cell in ws[column_letter]:
            # Abaikan tipe MergedCell agar tidak error
            if type(cell).__name__ == 'MergedCell':
                continue

            if cell.value:
                try:
                    if len(str(cell.value)) > max_length:
                        max_length = len(str(cell.value))
                except:
                    pass

        adjusted_width = (max_length + 2)
        ws.column_dimensions[column_letter].width = adjusted_width

    ws.column_dimensions['A'].width = 15  # Fix lebar kolom Kategori

    # Simpan File
    filename = "Dashboard_Input_Harga.xlsx"
    wb.save(filename)
    files.download(filename)
    print(f"✅ Laporan '{filename}' berhasil dibuat dan siap diunduh!")

