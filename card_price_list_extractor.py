def extract_number(val, is_float=False):
    s = str(val).lower().replace('rp', '').replace(' ', '')
    if is_float:
        # Untuk GB (Float): Ubah koma jadi titik, tapi jangan hapus titik yang sudah ada
        s = s.replace(',', '.')
        s = re.sub(r'[^\d.]', '', s)
        return float(s) if s else 0.0
    else:
        # Untuk Harga (Integer): Hapus titik ribuan
        s = s.replace('.', '')
        s = re.sub(r'[^\d]', '', s)
        return int(s) if s else 0

df_gt['gb_clean'] = df_gt['GB'].apply(lambda x: extract_number(x, is_float=True))
df_gt['price_clean'] = df_gt['Price'].apply(lambda x: extract_number(x, is_float=False))
df_gt['days_clean'] = pd.to_numeric(df_gt['Days'], errors='coerce').fillna(0).astype(int)

# Fungsi membuat ID unik per baris
def make_key(prov, gb, days, price):
    # Menyederhanakan float misal 5.0 dan 5 akan sama-sama menjadi string '5.0'
    return f"{prov}_{float(gb)}_{int(days)}_{int(price)}"

df_gt['match_key'] = df_gt.apply(lambda row: make_key(row['provider_clean'], row['gb_clean'], row['days_clean'], row['price_clean']), axis=1)

